let appendData = (objdata) => {
    let container = document.querySelector("#items");
    container.innerHTML = null;

    let count = 0;

    objdata.map((ele) => {
        if (ele.category == query || ele.name == query){
            count++;

            let box = document.createElement("div");
            box.setAttribute("id", "box");

             

            let image = document.createElement("img");
            image.src = ele.img_url;
            image.setAttribute("id","pic")
            image.addEventListener("click", function(){
                storeProduct(ele);
            })
            let sum=document.createElement("div")
        

            let name = document.createElement("p");
            name.innerText = ele.name;
            // name.setAttribute("id", "span1");

            let price = document.createElement("p");
            price.innerText = "₹ " + ele.price;
            // name.setAttribute("id", "span2");
               sum.append(name,price)
            box.append(image,sum);

            container.append(box);
        }
    });
    document.getElementById("count").innerText = count + "  RESULTS";
}

function storeProduct(ele){
    data.push(ele);
    console.log(data);
    localStorage.setItem("productsData", JSON.stringify(data));
    window.location.href = "cart.html";
}

let priceSort = () => {
    var selected = document.querySelector("#filterData").value;

    if (selected == "lth"){
        objdata.sort((a, b) => {
            return parseInt(a.price) - parseInt(b.price);
        });
        appendData(objdata);
    }
    else if (selected == "htl"){
        objdata.sort((a, b) => {
            return parseInt(b.price) - parseInt(a.price);
        });
        appendData(objdata);
    }
}





