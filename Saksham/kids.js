let priceSort = () => {
    var selected = document.querySelector("#filterData").value;

    if (selected == "lth"){
        objdata.sort((a, b) => {
            return parseInt(a.price) - parseInt(b.price);
        });
        appendData(objdata);
    }
    else if (selected == "htl"){
        objdata.sort((a, b) => {
            return parseInt(b.price) - parseInt(a.price);
        });
        appendData(objdata);
    }
}





